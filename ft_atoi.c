#include "libft.h"

int		ft_atoi(const char *str)
{
	char	*s;
	int		i;
	int		sign;
	int		res;

	res = 0;
	sign = 1;
	i = 0;
	s = (char *)str;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\r')
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res * sign);
}
